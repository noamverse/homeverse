export function getFeaturedProfiles() {
  return featuredProfileSlugs.map((slug) => articles.find((article) => article.slug === slug)).filter(Boolean);
}

export function getFeaturedStories() {
  return featuredStorySlugs.map((slug) => articles.find((article) => article.slug === slug)).filter(Boolean);
}

export function getArticlesBySection(section) {
  return articles.filter((article) => article.section === section);
}

export function getArticleBySlug(slug) {
  return articles.find((article) => article.slug === slug);
}
